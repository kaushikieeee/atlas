export function getTermsByFirstLetter(letter: string): DictionaryTerm[] {
  return terms.filter(t => t.term[0].toUpperCase() === letter.toUpperCase());
}

export function getTermBySlug(slug: string): DictionaryTerm | undefined {
  return terms.find(t => t.slug === slug);
}

export function searchTerms(query: string): DictionaryTerm[] {
  const q = query.toLowerCase();
  return terms.filter(t =>
    t.term.toLowerCase().includes(q) ||
    t.definition.toLowerCase().includes(q) ||
    t.explanation.toLowerCase().includes(q) ||
    t.category.toLowerCase().includes(q)
  );
}
